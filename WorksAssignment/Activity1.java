public class Activity1 {

    public static void main(String[] args) {
        System.out.println("Sum of Two Integers");
        int a = 10;
        int b = 20;
        int sum = a + b;
        System.out.println(sum);

        System.out.println("Sum of two floating point numbers");
        float c = 10.5f;
        float d = 20.5f;
        float sum1 = c + d;
        System.out.println(sum1);

        System.out.println("Sum of one integer and two floating point numbers");
        int e = 10;
        float f = 20.5f;
        float g = 30.5f;
        float sum2 = e + f + g;
        System.out.println(sum2);

        System.out.println("Sum of two integers stored in byte type");
        byte h = 10;
        byte i = 20;
        int sum3 = h + i;
        System.out.println(sum3);

        System.out.println("Sum of two integers stored in short type");
        short j = 10;
        short k = 20;
        int sum4 = j + k;

        System.out.println(sum4);
    }
}